<?php

namespace App\src\model;

/**
 * Class Comment
 * @package App\src\model
 */
class Comment
{
    private $id;
    private $author;
    private $comment;
    private $alert;
    private $dateAdded;


    /** ----------------------------RECUPERATION ID---------------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getId()
    {
        return $this->id;
    }


    /** ---------------------------- MAJ ID-----------------------------------------------------------------------------
     * @param $id
    -----------------------------------------------------------------------------------------------------------------*/
    public function setId($id)
    {
        $this->id = $id;
    }


    /** ----------------------------RECUPERATION POST ID----------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getPostId()
    {
        return $this->postId;
    }


    /** ---------------------------- MAJ POST ID------------------------------------------------------------------------
     * @param $postId
    -----------------------------------------------------------------------------------------------------------------*/
    public function setPostId($postId)
    {
        $this->postId = $postId;
    }


    /** ----------------------------RECUPERATION AUTEUR-----------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getAuthor()
    {
        return $this->author;
    }


    /** ---------------------------- MAJ AUTHOR-------------------------------------------------------------------------
     * @param $author
    -----------------------------------------------------------------------------------------------------------------*/
    public function setAuthor($author)
    {
        $this->author = $author;
    }


    /** ----------------------------RECUPERATION COMMENTAIRE------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getComment()
    {
        return $this->comment;
    }


    /** ---------------------------- MAJ COMMENTAIRES-------------------------------------------------------------------
     * @param $comment
    -----------------------------------------------------------------------------------------------------------------*/
    public function setComment($comment)
    {
        $this->comment = $comment;
    }

    /** ----------------------------RECUPERATION SIGNALEMENT------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getAlert()
    {
        return $this->alert;
    }


    /** ---------------------------- MAJ SIGNALEMENT--------------------------------------------------------------------
     * @param $alert
    -----------------------------------------------------------------------------------------------------------------*/
    public function setAlert($alert)
    {
        $this->alert = $alert;
    }

    /** ----------------------------RECUPERATION DATE-------------------------------------------------------------------
     * @return mixed
    -----------------------------------------------------------------------------------------------------------------*/
    public function getDateAdded()
    {
        return $this->dateAdded;
    }


    /** ---------------------------- MAJ DATE---------------------------------------------------------------------------
     * @param $dateAdded
    -----------------------------------------------------------------------------------------------------------------*/
    public function setDateAdded($dateAdded)
    {
        $this->dateAdded = $dateAdded;
    }
}

